import datetime
current_datetime = datetime.datetime.now()
future_datetime = current_datetime + datetime.timedelta(days=7)
print("Current Datetime:", current_datetime)
print("Future Datetime:", future_datetime)
# Output:
# Current Datetime: 2023-07-26 12:34:56.789012
# Future Datetime: 2023-08-02 12:34:56.789012
