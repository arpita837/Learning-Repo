# Print a single value
print("Hello, world!")
# Output: Hello, world!

# Print multiple values with a custom separator
print("Python", "is", "awesome", sep="-")
# Output: Python-is-awesome

# Printing variables
name = "Alice"
age = 30
print("Name:", name, "Age:", age)
# Output: Name: Alice Age: 30

# Changing the end character
print("Hello", end=" ")
print("World!")
# Output: Hello World!
